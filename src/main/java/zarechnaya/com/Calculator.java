package zarechnaya.com;

public class Calculator {
    public int getSum(int x, int y) {
        return x+y;
    }

    public int getMinus(int x, int y) {
        return x-y;
    }

    public int getDivide(int x, int y) {
        return x/y;
    }

    public int getMultiply(int x, int y) {
        return x*y;
    }
}
